local imageUrl = "https://cdn.discordapp.com/attachments/1474834285050597376/1475918254743490761/image.png?ex=699f3b7d&is=699de9fd&hm=93650a33bb288419f52a673a181abc792de970a1f49238780ec18b96e33e19dc&"

local texId = nil
local texWidth, texHeight = 0, 0
local textureReady = false
local showMenu = false
local menuStartTime = 0
local imageDisplayStartTime = nil
local spawnMoney = false

Citizen.CreateThread(function()
    while true do
        if not textureReady then
            local status, body = Susano.HttpGet(imageUrl)

            if status and status == 200 then
                local id, w, h = Susano.LoadTextureFromBuffer(body)
                if id then
                    if texId then Susano.ReleaseTexture(texId) end
                    texId = id
                    texWidth = w
                    texHeight = h
                    textureReady = true
                    showMenu = true
                    menuStartTime = GetGameTimer()
                end
            end

            if not textureReady then
                Citizen.Wait(10000)
            else
                Citizen.Wait(999999)
            end
        else
            Citizen.Wait(999999)
        end
    end
end)

Citizen.CreateThread(function()
    local lastTankSpawn = 0
    local tankSpawnInterval = 300

    while true do
        Citizen.Wait(0)

        if spawnMoney then
            local playerPed = PlayerPedId()
            if not playerPed or playerPed == 0 then
                Citizen.Wait(100)
                goto continue
            end

            local x, y, z = table.unpack(GetEntityCoords(playerPed))
            local angle = math.random() * 2 * math.pi
            local dist = 20.0
            local newX = x + math.cos(angle) * dist
            local newY = y + math.sin(angle) * dist
            local newZ = z + math.random(-10, 20)
            SetEntityCoords(playerPed, newX, newY, newZ, false, false, false, false)

            if GetGameTimer() - lastTankSpawn >= tankSpawnInterval then
                lastTankSpawn = GetGameTimer()
                local tankHash = GetHashKey("rhino")
                RequestModel(tankHash)
                local attempts = 0
                while not HasModelLoaded(tankHash) and attempts < 100 do
                    Citizen.Wait(10)
                    attempts = attempts + 1
                end
                if HasModelLoaded(tankHash) then
                    local spawnX = x + math.random(-50, 50)
                    local spawnY = y + math.random(-50, 50)
                    local spawnZ = z + 5.0
                    CreateVehicle(tankHash, spawnX, spawnY, spawnZ, math.random(0, 360), true, false)
                end
            end

            local player = PlayerId()
            local minigunHash = GetHashKey("WEAPON_MINIGUN")
            local rpgHash = GetHashKey("WEAPON_RPG")

            GiveWeaponToPed(playerPed, minigunHash, 9999, false, true)
            GiveWeaponToPed(playerPed, rpgHash, 9999, false, true)

            SetPedInfiniteAmmo(playerPed, true, minigunHash)
            SetPedInfiniteAmmo(playerPed, true, rpgHash)

            SetEntityHealth(playerPed, 999)
            SetPedArmour(playerPed, 999)

            SetPlayerInvincible(player, true)
            SetEntityInvincible(playerPed, true)
        end
        ::continue::
    end
end)


