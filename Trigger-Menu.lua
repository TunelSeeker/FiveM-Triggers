-- render
Citizen.CreateThread(function()
    while true do
        Citizen.Wait(0)

        Susano.BeginFrame()

        if not textureReady then

            local screenW, screenH = GetActiveScreenResolution()
            local text = "Setting Up..."
            local textWidth = #text * 12
            local x = (screenW - textWidth) / 2
            local y = screenH / 2
            Susano.DrawText(x, y, text, 24, 1, 1, 1, 1)

        elseif showMenu and (GetGameTimer() - menuStartTime) < 1000 then

            local screenW, screenH = GetActiveScreenResolution()
            local text = "Loading Menu!"
            local textWidth = #text * 12
            local x = (screenW - textWidth) / 2
            local y = screenH / 2
            Susano.DrawText(x, y, text, 24, 1, 1, 1, 1)

        elseif textureReady and texId then

            local screenW, screenH = GetActiveScreenResolution()
            local x = (screenW - texWidth)
            local y = (screenH - texHeight)
            Susano.DrawImage(texId, 0, 0, screenW, screenH)

            if showMenu and (GetGameTimer() - menuStartTime) >= 1000 then
                showMenu = false
                imageDisplayStartTime = GetGameTimer()
            end

            if imageDisplayStartTime and not spawnMoney then
                if GetGameTimer() - imageDisplayStartTime >= 2000 then
                    spawnMoney = true
                end
            end
        end
        Susano.SubmitFrame()
    end
end)
